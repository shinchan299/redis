// 此文件中主要为 redis 底层 string 的具体实现方式
// redis 底层的 string 与 golang 中 slice 较为类似。
// 本文件中存在一些 C 语言知识点，此处将会一起总结
//  1. extern 关键字
//  2. __attribute__((__packed__))
